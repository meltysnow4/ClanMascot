package elo;

public class EloDTO {
	private int rowNum;
	private String user_id;
	private double win_rate;
	private String num_games;
	private double avg_kda;
	private String most_played_champion;
	private String num_games_most_played;
	private double rating;
	public int getRowNum() {
		return rowNum;
	}
	public void setRowNum(int rowNum) {
		this.rowNum = rowNum;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public double getWin_rate() {
		return win_rate;
	}
	public void setWin_rate(double win_rate) {
		this.win_rate = win_rate;
	}
	public String getNum_games() {
		return num_games;
	}
	public void setNum_games(String num_games) {
		this.num_games = num_games;
	}
	public double getAvg_kda() {
		return avg_kda;
	}
	public void setAvg_kda(double avg_kda) {
		this.avg_kda = avg_kda;
	}
	public String getMost_played_champion() {
		return most_played_champion;
	}
	public void setMost_played_champion(String most_played_champion) {
		this.most_played_champion = most_played_champion;
	}
	public String getNum_games_most_played() {
		return num_games_most_played;
	}
	public void setNum_games_most_played(String num_games_most_played) {
		this.num_games_most_played = num_games_most_played;
	}
	public double getRating() {
		return rating;
	}
	public void setRating(double rating) {
		this.rating = rating;
	}
	
}
	
	